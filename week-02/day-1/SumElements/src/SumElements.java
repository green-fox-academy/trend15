public class SumElements {
    public static void main(String[] args) {
        int[] r = {54, 23, 66, 12};
        int sum =0;
        for (int i= 2;i<=3;i++){
            sum+=r[i];
        }
        System.out.println("The sum is: "+ sum);
    }
}

